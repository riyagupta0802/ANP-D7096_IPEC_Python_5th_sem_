'''to calculate the total cost of rice packets purchased from a grocery store'''

# input of price per packet from user
price_per_packet = float(input("Enter the price per packet: "))

# input of number of packets from user
number_of_packets = int(input("Enter the number of packets: "))

# calculation of total bill amount
print("Total Bill Amount is: ", price_per_packet * number_of_packets)