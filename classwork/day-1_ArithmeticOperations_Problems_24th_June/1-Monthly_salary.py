'''to calculate the total monthly salary of an employee by adding the fixed salary and incentive amount
'''

#input of fixed salary from input
fixed_salary = float(input("Enter the fixed salary: "))

#input of incentive amount from input
incentive_amount = float(input("Enter the incentive amount: "))

#calculation of total monthly salary    
print("Total monthly salary of an employee is: ", fixed_salary + incentive_amount)